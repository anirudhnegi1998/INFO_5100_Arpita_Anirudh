    private void payFundBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payFundBtnActionPerformed
        logr.info("Pay funds action performed");
        int selectedRow = corporateFundTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select An Event To Donate");
            System.out.println(selectedRow);
            return;
        }else{
            String money = JOptionPane.showInputDialog(null, "Enter The Amount You Want To Donate?", "Donation Window", JOptionPane.INFORMATION_MESSAGE);
        System.out.println(money);
        try{
        if (money.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter the Amount you want to donate in digits");
            return;
        }
        }catch(NullPointerException et){
            System.out.println("Cancel option");
            return;
        }
       
        EventEnterprise e = (EventEnterprise) corporateFundTable.getValueAt(selectedRow, 0);
        //Fund will be sent to System Admin and will be assigned to a hospital
        try{
            e.setFundsCollected(Integer.parseInt(money));
            System.out.println(e.getFundsCollected());
            JOptionPane.showMessageDialog(null, "You have donated " +money+ "$. Thank you for your contribution! " +"\n"+" Funds Sent to System Admin.");
        }catch(NumberFormatException ex){
      
             JOptionPane.showMessageDialog(null, "Please Enter The Amount you want to donate in digits");
        }
        }
        logr.info("Funds paid for fundraiser event");
    }//GEN-LAST:event_payFundBtnActionPerformed

